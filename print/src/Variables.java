public class Variables {
    public static void main(String[] args) {
        String text = "Chickens:";
        int number1 = 9000;
        System.out.println(text + "\n" + number1);

        String text2 = "Bacon (kg):";
        double number2 = 0.1;
        System.out.println(text2 + "\n" + number2);

        String text3 = "A tractor:";
        String number3 = "Zetor";
        System.out.println(text3 + "\n" + number3);

        System.out.println("\n" + "In a nutshell:");
        System.out.println(number1);
        System.out.println(number2);
        System.out.println(number3);
    }
}
